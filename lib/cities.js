export function cityToSlug(cityName) {
  return cityName.toLowerCase().replace(/\s+/g, '-');
}

export function buildSlug(citySlug, serviceSlug) {
  if (serviceSlug) return `plumber-${citySlug}-${serviceSlug}`;
  return `plumber-${citySlug}`;
}

export function parseSlug(fullSlug) {
  if (!fullSlug || !fullSlug.startsWith('plumber-')) return null;
  const withoutPrefix = fullSlug.replace(/^plumber-/, '');
  // Sort services by slug length (descending) to match longer slugs first
  const sortedServices = [...SERVICES].sort((a, b) => b.slug.length - a.slug.length);
  const service = sortedServices.find(s => withoutPrefix.endsWith(`-${s.slug}`));
  if (service) {
    const citySlug = withoutPrefix.slice(0, -(service.slug.length + 1));
    return { citySlug, service };
  }
  return { citySlug: withoutPrefix, service: null };
}

// Get city by slug - merges SEED_CITIES with CITY_DATA
export function getCityBySlug(slug) {
  const seedCity = SEED_CITIES.find(c => cityToSlug(c.name) === slug);
  if (!seedCity) return null;
  const cityData = CITY_DATA[seedCity.name];
  if (!cityData) return null;
  return { ...seedCity, ...cityData };
}

